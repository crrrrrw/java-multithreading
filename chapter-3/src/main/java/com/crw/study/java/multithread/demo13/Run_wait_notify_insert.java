/**
 * P177
 *
 * 等待/通知，交叉执行
 * 创建20个线程，其中10个线程讲数据备份到A数据库，另10个线程讲数据备份到B数据库，并且
 * 备份A数据库与备份B数据库交叉执行
 */
public class Run_wait_notify_insert {
    public static void main(String[] args) {
        DBTools dbTools = new DBTools();
        for (int i = 0; i < 20; i++) {
            BackupB output = new BackupB(dbTools);
            output.start();
            BackupA input = new BackupA(dbTools);
            input.start();
        }
    }
}

